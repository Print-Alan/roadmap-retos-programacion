 #Operadores aritméticos

#Suma
print(f"suma: 10 + 3 = {10 + 3}")

#resta
print(f"resta: 10 - 3 = {10 - 3}")

#Multiplicacion
print(f"Multiplicacion: 10 * 3 = {10 * 3}")

#Divicion
print(f"divicion: 10 / 3 = {10 / 3}")

#Modulo
print(f"modulo: 10 % 3 = {10 % 3}")

#Exponente
print(f"Exponente: 10 ** 3 = {10 ** 3}")

#Divicion entera
print(f"Divicion entera: 10 // 3 = {10 // 3}")


 #Operadores de comparación

#Igualdad
print(f"10 == 3 {10 == 3}") # ¿10 es igual a 3? : False

#Desigualdad 
print(f"10 != 10 {10 != 3}") # ¿10 es distinto de a 3? : True

#Mayor que
print(f"10 > 3 {10 > 3}") # ¿10 Mayor que 3? : True

#Menor que
print(f"10 < 3 {10 < 3}") # ¿10 manor que 3? : False

#Mayor o igual que
print(f"10 >= 3 {10 >= 3}") # ¿10 mayor que 3? : True

#Menor o igual que
print(f"10 <= 3 {10 <= 3}") # ¿10 menor o igual a 3? : false


 #Operadores logicos

#And lógico
print(f"10 + 3 == 13 and 5 - 1 == 4 es {10 + 3 == 13 and 5 - 1 == 4}") #Las dos operaciones deben ser correctas para el true

#Or lógico
print(f"10 + 3 == 12 or 5 - 1 == 4 es {10 + 3 == 12 or 5 - 1 == 4}") #Una operación debe ser correcta para el true

#Not lógico
print(f"not 10 + 3 == 14 es {not 10 + 3 == 14}")#

